giorni = ["Lun", "Mar", "Mer", "Gio", "Ven", "Sab", "Dom"]

print("INDICI POSITIVI:")
print(f"giorni[0] = {giorni[0]}")  # primo
print(f"giorni[6] = {giorni[6]}")  # ultimo

print("\nINDICI NEGATIVI:")
print(f"giorni[-1] = {giorni[-1]}")
print(f"giorni[-7] = {giorni[-7]}")
print(f"giorni[-3] = {giorni[-3]}")

# Trucco: giorni[6] e giorni[-1] sono la stessa cosa
