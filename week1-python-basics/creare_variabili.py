# Simula portafoglio:
# - soldi = 50
# - Spendi 12 per pranzo
# - Ricevi 20 da un amico
# - Spendi 7 per caffè
# - Stampa quanto ti resta
# - Stampa anche quanto hai speso in totale

# TUO CODICE QUI:
soldi = 50
spesa_pranzo = 12
donazione_amico = 20
spesa_caffe = 7

soldi_totali = soldi + donazione_amico
spesa_totale = spesa_caffe + spesa_pranzo
saldo_restante = soldi_totali - spesa_totale

print("Oggi mi resta", saldo_restante)
print("Oggi ho speso", spesa_totale)
